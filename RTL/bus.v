module bus(

input [31:0] addr,

output rom_sel,
output ram_sel,
output gpio_sel,
output uart_sel,
output timer_sel

);

//==================================
// ROM : 0x00000000 - 0x0FFFFFFF
//==================================

assign rom_sel =
(addr[31:28] == 4'h0);

//==================================
// RAM : 0x10000000 - 0x1FFFFFFF
//==================================

assign ram_sel =
(addr[31:28] == 4'h1);

//==================================
// GPIO : 0x20000000 - 0x20000FFF
//==================================

assign gpio_sel =
(addr >= 32'h20000000) &&
(addr <  32'h20001000);

//==================================
// UART : 0x20001000 - 0x20001FFF
//==================================

assign uart_sel =
(addr >= 32'h20001000) &&
(addr <  32'h20002000);

//==================================
// TIMER : 0x20002000 - 0x20002FFF
//==================================

assign timer_sel =
(addr >= 32'h20002000) &&
(addr <  32'h20003000);

endmodule
